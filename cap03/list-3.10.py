# Exemplos de composição com números inteiros

idade = 22

print("[%d]" % idade)

print("[%03d]" % idade)

print("[%010d]" % idade)

print("[%20d]" % idade)

print("[%020d]" % idade)

print("[%-020d]" % idade)

print("[%-20d]" % idade)

print("[%3d]" % idade)

print("[%100d]" % idade)

print("[%-3d]" % idade)